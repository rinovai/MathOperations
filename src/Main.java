public class Main {
    public static void main(String[] args) {

        int a = 40;
        int b = 30;
        int c = 10;
        int d = 20;
        System.out.println(sum(a, b, c, d));
    }

    public static int sum(int value1, int value2, int value3, int value4) {

        int result = value1 + value2 + value3 + value4;
        return result;


    }
}